def gerar_epc(patrimonio: str, serial: str) -> str:
    prefixo = "4D41"

    cod_item = "".join(filter(str.isdigit, patrimonio)).zfill(6)
    serial = str(serial).zfill(14)

    epc = (prefixo + cod_item + serial)[:24]
    return epc


